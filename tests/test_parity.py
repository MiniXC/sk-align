"""
Parity tests between sk-align and the pykaldi-based aligner.

These tests verify that the sk-align implementation produces results
matching (or close to) the pykaldi reference implementation.

Two modes:
1. **Offline** — compare against pre-generated reference data (JSON/NPY files)
2. **Online** — run both implementations and compare (requires pykaldi conda env)

Run with:  pytest tests/test_parity.py -v
"""

import json
import os
from pathlib import Path

import numpy as np
import pytest

from sk_align.mfcc import MfccOptions, compute_mfcc
from sk_align.aligner import Aligner, PrecomputedScorer

REFERENCE_DIR = Path(__file__).parent / "reference_data"


def _all_ref_dirs():
    """Return all reference data subdirectories."""
    if not REFERENCE_DIR.exists():
        return []
    return sorted(d for d in REFERENCE_DIR.iterdir() if d.is_dir())


# ---------------------------------------------------------------------------
# Offline parity tests (using pre-generated reference data)
# ---------------------------------------------------------------------------

class TestOfflineParity:
    """Compare against pre-generated reference alignment data.

    Reference data is generated by running:
        scripts/generate_reference.py
    from the pykaldi conda environment.
    """

    @pytest.fixture(params=_all_ref_dirs() or [None],
                    ids=[d.name for d in _all_ref_dirs()] or ["none"])
    def ref_dir(self, request):
        if request.param is None:
            pytest.skip("No reference data found. Run scripts/generate_reference.py first.")
        return request.param

    def test_alignment_output_format(self, ref_dir, model_dir):
        """Alignment output should have the correct structure."""
        expected_path = ref_dir / "expected_output.json"
        if not expected_path.exists():
            pytest.skip("No expected_output.json in reference data")

        with open(expected_path) as f:
            expected = json.load(f)

        assert isinstance(expected, list)
        for entry in expected:
            assert "word" in entry
            assert "start" in entry
            assert "end" in entry
            assert entry["start"] >= 0
            assert entry["end"] > entry["start"]

    def test_mfcc_shape_matches(self, ref_dir, model_dir):
        """MFCC dimensions should match between implementations."""
        mfcc_path = ref_dir / "mfcc_features.npy"
        audio_path = ref_dir / "audio.npy"
        if not mfcc_path.exists() or not audio_path.exists():
            pytest.skip("Reference MFCC or audio data not found")

        ref_mfcc = np.load(mfcc_path)
        audio = np.load(audio_path)

        opts = MfccOptions(
            sample_freq=16000.0,
            use_energy=False,
            num_mel_bins=40,
            num_ceps=40,
            low_freq=20.0,
            high_freq=-400.0,
            dither=0.0,
        )
        wav = audio * (1 << 15)
        our_mfcc = compute_mfcc(wav, opts)

        assert our_mfcc.shape == ref_mfcc.shape, (
            f"MFCC shape mismatch: ours={our_mfcc.shape}, ref={ref_mfcc.shape}"
        )

    def test_mfcc_values_close(self, ref_dir, model_dir):
        """MFCC values should be reasonably close to pykaldi's."""
        mfcc_path = ref_dir / "mfcc_features.npy"
        audio_path = ref_dir / "audio.npy"
        if not mfcc_path.exists() or not audio_path.exists():
            pytest.skip("Reference MFCC or audio data not found")

        ref_mfcc = np.load(mfcc_path)
        audio = np.load(audio_path)

        opts = MfccOptions(
            sample_freq=16000.0,
            use_energy=False,
            num_mel_bins=40,
            num_ceps=40,
            low_freq=20.0,
            high_freq=-400.0,
            dither=0.0,
        )
        wav = audio * (1 << 15)
        our_mfcc = compute_mfcc(wav, opts)

        # Allow some tolerance — dither and numerical differences
        # are expected between implementations
        diff = np.abs(our_mfcc - ref_mfcc)
        mean_diff = diff.mean()
        max_diff = diff.max()
        print(f"\n  MFCC difference: mean={mean_diff:.6f}, max={max_diff:.6f}")

        # MFCC values typically range from -50 to 50; mean diff < 5.0 is good
        # Note: pykaldi uses dither by default, our code uses dither=0
        assert mean_diff < 5.0, f"Mean MFCC difference too large: {mean_diff}"

    def test_word_count_matches(self, ref_dir, model_dir):
        """Number of aligned words should match."""
        expected_path = ref_dir / "expected_output.json"
        metadata_path = ref_dir / "metadata.json"
        if not expected_path.exists() or not metadata_path.exists():
            pytest.skip("Reference data not found")

        with open(expected_path) as f:
            expected = json.load(f)
        with open(metadata_path) as f:
            meta = json.load(f)

        assert len(expected) == len(meta["words"])


